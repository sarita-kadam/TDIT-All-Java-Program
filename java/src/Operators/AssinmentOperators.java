package Operators;

public class AssinmentOperators {
    public static void main(String[] args) {

        int a = 7;

        System.out.println(a += 1);  // 8
        System.out.println(a -= 2);  //6
        System.out.println(a *= 3);  //18
        System.out.println(a /= 4);  //4
        System.out.println(a %= 5);  //0
        System.out.println(a <<= 2); //
    }
}
//-	(=) The assignment operator is used to assign a value to any variable.
//
//-	It has right-to-left associativity.